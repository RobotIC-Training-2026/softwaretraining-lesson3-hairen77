/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        // 包头检测
        if (rx_index == 0 && rx_byte != 0xCC)
        {
            // 等待包头CC开始
        }
        else
        {
            rx_buffer[rx_index++] = rx_byte;
        }

        // 收够头部+有效距离字节
        uint8_t expected_len = 4; // 头1+头2+ID+num
        if (rx_index >= 4)         // 接收到num字节
        {
            expected_len += rx_buffer[3]; // rx_buffer[3] = num，距离个数
        }

        if (rx_index >= expected_len)
        {
            frame_ready = 1;
            rx_index = 0;
        }

        // 继续接收下一个字节
        HAL_UART_Receive_IT(&huart1, &rx_byte, 1);
    }
}
